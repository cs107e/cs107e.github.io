#ifndef CONSOLE_H_INCLUDED
#define CONSOLE_H_INCLUDED

/* When called, this function takes control of the screen and keyboard.
   It should not return. */    
void console_start();

/* A function to print a string to the screen. The console provides
   this function so simple commands can output to the screen. It should
   print the string at the current cursor position.*/
void console_print(char* str);

#endif


